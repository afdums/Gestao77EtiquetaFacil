    public void setLayout(Layout layout, Emitente emitente){
        this.emitente = emitente;
        textRazao.setText(emitente.getRazao());
        if(layout != null){
            System.out.println("SETANDO LAYOUT");
            this.layout = layout;
            textCodigo.setText(layout.getCodigo());
            textCodigo.setEnabled(false);
            comboTipo.setSelectedIndex(layout.getTipo());
            comboTipo.setEnabled(false);
            textQuantidadeCaixa.setText(Integer.toString(layout.getQuantidadeCaixa()));
            textPulaEtiqueta.setText(Integer.toString(layout.getPulaEtiqueta()));
            textImpressora.setText(layout.getImpressora());
            selectValidaImpressao.setSelected(layout.isValidaImpressaoCaixa());
            textComprimentoChave.setText(Integer.toString(layout.getComprimentoChave()));
            comboTipo.requestFocus();
            fonteLayoutAux = layout.getFonteLayout();
            populaPropriedades();
            populaPropriedadesTela();
            novo = false;
        }else{
            textCodigo.requestFocus();
            fonteLayoutAux = "";
            novo = true;
        }
        
    }
    
    public Layout getObjetoLayout(){
        return layout;
    }
    
    private void salvar(){
        LayoutDAO layoutDAO = new LayoutDAO();
        if(novo == true){
            System.out.println("NOVO LAYOUT");
            layout = new Layout(emitente.getId(), textCodigo.getText(), comboTipo.getSelectedIndex(), Integer.parseInt(textQuantidadeCaixa.getText()), selectValidaImpressao.isSelected(), fonteLayoutAux, Integer.parseInt(textComprimentoChave.getText()));
            layoutDAO.insert(layout);
            //layout = layoutDAO.selectWhereCodigo(emitente.getId(), textCodigo.getText());
        }else{
            layout.setIdEmitente(emitente.getId());
            layout.setCodigo(textCodigo.getText());
            layout.setTipo(comboTipo.getSelectedIndex());
            layout.setQuantidadeCaixa(Integer.parseInt(textQuantidadeCaixa.getText()));
            layout.setValidaImpressaoCaixa(selectValidaImpressao.isSelected());
            layout.setFonteLayout(fonteLayoutAux);
            layout.setPulaEtiqueta(Integer.parseInt(textPulaEtiqueta.getText()));
            layout.setComprimentoChave(Integer.parseInt(textComprimentoChave.getText()));
            layout.setImpressora(textImpressora.getText());
            layoutDAO.update(layout);            
        }
        LayoutPropriedadeDAO layoutPropriedadeDAO = new LayoutPropriedadeDAO();
        layoutPropriedadeDAO.deleteLote(layout.getId());
        for(int i = 0; i < propriedades.size(); i++){
            propriedades.get(i).setIdLayout(layout.getId());
            propriedades.get(i).setId(0);            
        }
        layoutPropriedadeDAO.insertLote(propriedades);        
    }
    
    private void populaPropriedadesTela(){
        
        DefaultTableModel modeloTable = (DefaultTableModel) jTable1.getModel();
        
        while (modeloTable.getRowCount() > 0) {
            modeloTable.removeRow(0);
        }
        
        if(propriedades != null){
            for(int i = 0; i < propriedades.size(); i++){
                modeloTable.addRow(new Object[] {
                    i,
                    propriedades.get(i).getPropriedade(),
                    propriedades.get(i).retornaOrigemString(),
                    propriedades.get(i).retornaChaveUnica()});
            }
        }
    }
    
    private void populaPropriedades(){
       LayoutPropriedadeDAO layoutPropriedadeDAO = new LayoutPropriedadeDAO();
       propriedades = layoutPropriedadeDAO.selectAllLayoutPropriedades(layout.getId());        
    }
    
    private boolean validar(){
        //Verifica se não existe outro usuario com o mesmo login
        if(novo){
            LayoutDAO layoutDAO = new LayoutDAO();
            Layout layoutAux = layoutDAO.selectWhereCodigo(emitente.getId(), textCodigo.getText());
            if(layoutAux != null){
                JOptionPane.showMessageDialog(rootPane, "Layout já cadastrado com código informado para este emitente","Layout já cadastrado", JOptionPane.ERROR_MESSAGE);
                textCodigo.requestFocus();
                return false;                
            }
        }
        
        return true;
    }

