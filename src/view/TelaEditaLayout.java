    private void buttonFonteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonFonteActionPerformed
        // TODO add your handling code here:
        TelaEditaFonteLayout telaEditaFonteLayout = new TelaEditaFonteLayout(pai, true);
        /*if(layout != null)
            telaEditaFonteLayout.setFonteLayout(layout.getFonteLayout());
        else*/
            telaEditaFonteLayout.setFonteLayout(fonteLayoutAux);
        
        telaEditaFonteLayout.show();

        if(telaEditaFonteLayout.getReturnStatus()==1){
            fonteLayoutAux = telaEditaFonteLayout.getReturnFont();
            
            //fazer tratamento dos campos variaveis da etiqueta
            int comprimento = fonteLayoutAux.length();
            int posHash = -1;
            
            List<LayoutPropriedade> propriedadesAux = new ArrayList<LayoutPropriedade>();
            
            System.out.println("Comprimento: " + comprimento);
            for (int i=0; i < comprimento; i++) {
                if(fonteLayoutAux.charAt(i) == '#')
                    posHash = i;
                
                if((fonteLayoutAux.charAt(i) == '^' || fonteLayoutAux.charAt(i) == ',') && posHash != -1){
                    String propAux = fonteLayoutAux.substring(posHash, i);
                    System.out.println("Propriedade: " + propAux);
                    posHash = -1;
                    propriedadesAux.add(new LayoutPropriedade(propAux));
                }
                
            }
            
            int achou = 0;
            for(int i = 0; i < propriedades.size(); i++){
                achou = 0;
                for(int x = 0; x < propriedadesAux.size(); x++){
                    if(propriedades.get(i).getPropriedade().equals(propriedadesAux.get(x).getPropriedade())){
                        achou = 1;
                        x = propriedadesAux.size();
                    }
                }
                if(achou == 0){
                    propriedades.remove(i);
                }
                
            }
            
            for(int i = 0; i < propriedadesAux.size(); i++){
                achou = 0;
                for(int x = 0; x < propriedades.size(); x++){
                    if(propriedades.get(x).getPropriedade().equals(propriedadesAux.get(i).getPropriedade())){
                        achou = 1;
                        x = propriedades.size();
                    }
                }
                if(achou == 0){
                    System.out.println("Adicionando: " + propriedadesAux.get(i).getPropriedade());
                    propriedades.add(propriedadesAux.get(i));
                }
            }
            
            for(int i = 0; i < propriedades.size(); i++){
                if(propriedades.get(i).getOrigem() == 0){
                    System.out.println("Valida Origem");
                    propriedades.get(i).setOrigem(propriedades.get(i).validaOrigem());
                }
            }
            //populaPropriedades();
            populaPropriedadesTela();
            
        }
    }//GEN-LAST:event_buttonFonteActionPerformed

