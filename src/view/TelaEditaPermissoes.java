    public void populaTabela(){
        
        modeloTable = (DefaultTableModel) jTable1.getModel();
        
        while (modeloTable.getRowCount() > 0) {
            modeloTable.removeRow(0);
        }
        try{
            PermissaoDAO permissaoDAO = new PermissaoDAO();
            List<String> permissoes = permissaoDAO.selectAll(VariavelGlobal.getInstancec().getUsuarioLogado());
            List<String> funcoes = NomeTela.retornaTelas();
            if(funcoes != null){
                funcoes.forEach((String funcao) -> {
                    boolean liberado = false;
                    
                    if(permissoes != null){
                        for(int i = 0; i < permissoes.size(); i++){
                            if(permissoes.get(i).equals(funcao))
                                liberado = true;
                        }
                    }
                    
                    modeloTable.addRow(new Object[] { funcao,
                        liberado});
                });
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
