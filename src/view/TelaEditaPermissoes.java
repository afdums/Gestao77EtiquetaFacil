    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        List<Permissao> novasPermissoes = new ArrayList<Permissao>();
        String login = VariavelGlobal.getInstancec().getUsuarioLogado();
        
        PermissaoDAO permissaoDAO = new PermissaoDAO();
        permissaoDAO.deleteUsuario(login);
        
        for(int i = 0; i < jTable1.getRowCount(); i++){
            //System.out.println(jTable1.getValueAt(i,0).toString() + " - " + jTable1.getValueAt(i,1).toString());
            if(jTable1.getValueAt(i,1).toString().equals("true")){
                novasPermissoes.add(new Permissao(login, jTable1.getValueAt(i,0).toString()));
            }            
        }
        permissaoDAO = new PermissaoDAO();
        permissaoDAO.insertLote(novasPermissoes);
        doClose(RET_OK);
    }//GEN-LAST:event_btnSalvarActionPerformed
    
