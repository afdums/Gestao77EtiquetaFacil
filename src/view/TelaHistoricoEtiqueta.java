    private void consultaHistorico() {

        LogAlteracaoDAO logAlteracaoDAO = new LogAlteracaoDAO();
        List<LogAlteracao> logAlteracoes = logAlteracaoDAO.selectAll("etiqueta", textEtiqueta.getText());
        try {            
            BufferedWriter out = new BufferedWriter(
                    new FileWriter("logAlteracao.txt", false));
            out.write("Log Etiqueta: " + textEtiqueta.getText() +  "\n");
            for(LogAlteracao logAlteracao : logAlteracoes){
                out.write("\n\nUsuario: " + logAlteracao.getLogin());
                out.write("\nData: " + logAlteracao.getData());
                out.write("\nAlteração: " + logAlteracao.getAlteracao());
            }
            out.write("\n\nFim do log, gerado em: " + DataUtil.dataHoraAtual());
            out.close();
            Desktop.getDesktop().open(new File("logAlteracao.txt"));
        } catch (IOException e) {
            // possíveis erros são tratados aqui
        }

    }

