    public boolean valida() {

        if (layout == null) {
            LayoutDAO layoutDAO = new LayoutDAO();
            System.out.println("Layout selecionado: " + comboLayout.getSelectedItem().toString());
            layout = layoutDAO.selectWhereCodigo(emitente.getId(), comboLayout.getSelectedItem().toString());
        }

        if (textEtiqueta.getText().length() != layout.getComprimentoChave()) {
            JOptionPane.showMessageDialog(this, "Código de barras não tem o comprimento informado no layout", "Código de Barras Inválido", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        EtiquetaDAO etiquetaDAO = new EtiquetaDAO();

        System.out.println("Emitente Imp: " + emitente.getId());

        etiquetaIndividual = etiquetaDAO.selectWhereEmitenteChave(emitente.getId(), textEtiqueta.getText());

        System.out.println("A");

        if (etiquetaIndividual == null) {
            JOptionPane.showMessageDialog(this, "Etiqueta inexistente", "Etiqueta inexistente", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        System.out.println("B");

        return true;
    }

